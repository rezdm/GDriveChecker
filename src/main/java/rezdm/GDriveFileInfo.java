package rezdm;

class GDriveFileInfo {
    String file_id;
    String folder_id;
    String path;
    String name;

    GDriveFileInfo(String file_id, String folder_id, String path, String name) {
        this.file_id = file_id;
        this.folder_id = folder_id;
        this.path = path;
        this.name = name;
    }

}
